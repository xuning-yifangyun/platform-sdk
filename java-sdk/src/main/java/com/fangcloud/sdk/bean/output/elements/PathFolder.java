package com.fangcloud.sdk.bean.output.elements;

public class PathFolder {
    private String type;
    private long id;
    private String name;

    public PathFolder() {

    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
